// https://adventofcode.com/2023/day/23

var aLongWalk = function (input) {
  const DIR = [0, 1, 0, -1, 0];
  const slopes = {
    '>': [0, 1],
    v: [1, 0],
    '<': [0, -1],
    '^': [-1, 0],
  };
  const grid = getGrid(input);
  const rows = grid.length;
  const cols = grid[0].length;
  const [sr, sc] = [0, grid[0].indexOf('.')];
  const [er, ec] = [rows - 1, grid[rows - 1].indexOf('.')];
  const visited = new Set();
  let maxStep = 0;
  move(sr, sc, 0);

  //grid.map((line) => console.log(line.join('')));
  return maxStep;

  function move(r, c, steps) {
    if (!isValidCell(r, c)) return;

    const key = getKey(r, c);
    visited.add(key);

    if (r === er && c === ec) {
      maxStep = Math.max(maxStep, steps);
      visited.delete(key);
      return;
    }

    if (grid[r][c] === '.') {
      for (let d = 0; d < 4; d++) {
        const [nr, nc] = [r + DIR[d], c + DIR[d + 1]];
        move(nr, nc, steps + 1);
      }
    } else {
      const [dr, dc] = slopes[grid[r][c]];
      const [nr, nc] = [r + dr, c + dc];
      move(nr, nc, steps + 1);
    }

    visited.delete(key);
  }

  function getKey(row, col) {
    return `${row},${col}`;
  }

  function getGrid(input) {
    return input.split(/\n/).map((line) => line.split(''));
  }

  function isValidCell(row, col) {
    return (
      row >= 0 &&
      row < rows &&
      col >= 0 &&
      col < cols &&
      !visited.has(getKey(row, col)) &&
      '^v><.'.includes(grid[row][col])
    );
  }
};

