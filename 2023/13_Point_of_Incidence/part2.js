var pointOfIncidence = function (input) {
  let result = 0;
  const patterns = getData(input);
  for (const pattern of patterns) {
    result += findPatternValue(pattern);
  }
  return result;

  function getData(input) {
    return input.split('\n\n').map((block) => block.split('\n'));
  }

  function transpose(pattern) {
    const m = pattern.length;
    const n = pattern[0].length;
    const newGrid = new Array(n).fill('');
    for (let i = 0; i < m; i++) {
      for (let j = 0; j < n; j++) {
        newGrid[j] += pattern[i][j];
      }
    }
    return newGrid;
  }

  function isReflection(pattern, index) {
    let left = index - 1;
    let right = index;
    let count = 0;
    while (left >= 0 && right < pattern.length) {
      count += isAlternativeReflection(pattern[left], pattern[right]);
      if (count > 1) {
        return false;
      }
      left--;
      right++;
    }
    return count === 1;
  }

  function isAlternativeReflection(patternLeft, patternRight) {
    if (patternLeft === patternRight) return 0;

    let diff = 0;
    for (let i = 0; i < patternLeft.length; i++) {
      if (patternLeft[i] !== patternRight[i]) {
        if (++diff > 1) break;
      }
    }
    return diff;
  }

  function findPatternValue(pattern) {
    for (let i = 1; i < pattern.length; i++) {
      if (isReflection(pattern, i)) {
        return i * 100;
      }
    }

    const transposed = transpose(pattern);
    for (let i = 1; i < transposed.length; i++) {
      if (isReflection(transposed, i)) {
        return i;
      }
    }
  }
};

